        	<h3><strong>Les comptes :</strong></h3>
            <h5>
        		Vous devez avoir seulement un compte IG, et un compte forum. Il est interdit d'en avoir plus.<br/>
                Il est strictement interdit de partager vos informations de compte. Votre nom doit être utilisé à <br />
                l'aide du modèle Prénom_Nom.
       	  </h5>
            <br /><br />
            <h3><strong>Utilisation de bug :</strong></h3>
            <h5>
            	Il est strictement interdit d'utiliser des bugs de script sur notre serveur ainsi que sur le site/forum.
            </h5>
            <br /><br />
            <h3><strong>Stunt :</strong></h3>
            <h5>
            	Il est strictement interdit de faire du stunt (Cascade...) sur Los Santos, il y'a des serveurs spéciaux pour ça.
            </h5>
            <br /><br />
            <h3><strong>Intouchable :</strong></h3>
            <h5>
                A partir du moment où vous êtes nouveau (lvl1) en ville (débutant) vous êtes compris "intouchable" , les nouveaux sur le serveur sont "intouchable", il est interdit de les braquer, kidnapper, ils "découvrent" le roleplay du serveur.
                En tant qu'intouchable vous n'avez pas le droit de disposez d'arme ou de drogue. Vous pouvez fréquenter des mafias / gangs sans y être dans leur effectif officiel. Biensur si un joueur vous vend des armes / drogues vous devez les refuser.
                Vous ne participez pas aux brawls / fusillades..
            </h5>
            <br /><br />
            <h3><strong>Utilisation du /me et /do en scène :</strong></h3>
            <h5>
            	Il est très conseillé d'utiliser les /me et /do en scène pour donner des détails sur vos actions. Si quelqu’un vous pose une question en /do, vous devez obligatoirement y répondre.
Ses deux commandes sont complètements OOC, il ne doit donc pas y avoir de mensonges. Cela sera considéré comme mensonge OOC.
            </h5>
            <br /><br />
            <h3><strong>Faireplay :</strong></h3>
            <h5>
            	Soyez faireplay, ne vous avantagez pas toujours, sachez "perdre".
            </h5>
            <br /><br />
            <h3><strong>Conduite Rôleplay :</strong></h3>
            <h5>
            	Que vous êtes dans une campagne, dans une autre ville déserte, la conduite doit rester roleplay. Arrêter vous aux stop (Les feux rouges ne conptent pas). Si vous roulez sans ralentirs aux intersections d'une façon NO-RP. Cela sera considéré comme conduite non-roleplay.
            </h5><br /><br />
            <h3><strong>Les Scènes :</strong></h3>
            <h5>
            	A partir du moment ou vous êtes en scène, vous n'avez pas le droit de vous déconnectez sans rien dire (Il existe les urgences), tâchez de prévenir en OOC avant, favorisez le /chu (qui permet de chuchoter) pour parler en OOC à une personne. Donc une déconnection puis reconnection non justifiée sera sévèrement sanctionnée.
Cette règle est aussi valable pour l’hôpital (Déconnexion pour éviter de payer l'hosto).
            </h5><br /><br />
            <h3><strong>Les contacts avec les admins IG :</strong></h3>
            <h5>
            	Évitez de faire des rapports inutiles, si vous savez que l'action non-roleplay est déjà passée, penser à screen au part avant, puis à poster sur le forum.
Évitez les /i inutiles, posez vos questions directement et évitez de faire plusieurs /i pour dire une chose comme ci-dessous :
/i Vien.
/i Tu peux te tp ?
            </h5><br /><br />
            <h3><strong>Les insultes :</strong></h3>
            <h5>
            	Les insultes ingame sont totalement tolérées, les insultes OOC sont interdites, gardez vos propos pour vous pour évitez un baissement direct.
            </h5><br /><br />
            <h3><strong>La prison :</strong></h3>
            <h5>
            	Si vous êtes en prison IC, vous pourrez jouer une scène, si vous êtes en prison OOC, il est interdit de faire de scène. Pour l'utilisation du téléphone c'est complètement interdit en prison.
            </h5><br /><br />
            <h3><strong>Les permis :</strong></h3>
            <h5>
            	Les permis sont disponnibles à l'auto-école, le permis de port d'arme est distribué par la LSPD, le tarifs général est de 10 000$.
            </h5><br /><br />
            <h3><strong>Drive By :</strong></h3>
            <h5>
            	Il est autorisé de Drive By si : Vous êtes passagé d'un véhicule avec un conducteur . Il est interdit de Drive By en étant conducteur. Les armes que vous avez le droit son MP5 , Colt 45 , Silencieux , Uzi , Tec 9 et interdit de Drie By avec AK47 , M4 , Rifle ( Vous n'êtes pas Chuck Norris ) .
            </h5><br /><br />
            <h3><strong>Le "droit" de tuer :</strong></h3>
            <h5>
            	Vous avez le droit de tuer à partir du moment que le joueur et vous soiez niveau 2, vous devez avoir une raison valable, dans le cas contraire cela sera considéré comme du deathmatch.
            </h5><br /><br />
            <h3><strong>Les Factions :</strong></h3>
            <h5>
            	Il est possible de rejoindre une faction qu'a partir du niveau 2, vous pouvez avant fréquenté une faction sans y être dans l'effectif officiel. Si vous êtes un simple "habitant" du quartier ou quelqu’un qui la fréquente, vous aurez le droit de participez aux fusillades / brawl.<br />
Sachez que si vous ne respectez pas les 2 jours civil, vous aurez un baissement de 1 jour pour le finir. Cette règle ne s'applique pas pour les factions illégales.
            </h5><br /><br />
            <h3><strong>Les Jours Civil :</strong></h3>
            <h5>
            	Si vous quittez une faction , vous devriez attendre 1 jour Civil .
            </h5><br /><br />
            <h3><strong>Tricherie :</strong></h3>
            <h5>          
            	Il est interdit d'utiliser des logiciels de tricherie, ou de tricher, sachez que si vous demander le logiciel, recevez de l'argent, demandez... sans nous prévenir et nous rendre celle-ci, vous devenez complice. Cela sera donc considéré comme complicité de tricherie. Le bannissement sera donc définitif.
            </h5><br /><br />
            <h3><strong>Les Délires OOC :</strong></h3>
            Les Délires OOC  sont interdit à LS parcontre à SF et LV , ils sont autorisés .
            <h5><br /><br />
            <h3><strong>Provocation envers les factions gouvernementales : (LSPD, Maire...)</strong></h3>
            <h5>
            	Il est autorisé de provoquer les factions gouvernementales, mais à un certain degré : les provoquer en leurs disant des phrases, s'embrouiller normalement, cela sera autorisé, si vous passez devant la LSPD en moto plusieurs fois sans casque, puis leurs crier dessus des insultes, cela sera considéré comme abusive. Vous cherchez donc l'action.
            </h5><br /><br />
            <h3><strong>Racket :</strong></h3>
            <h5>
            	Il est autorisé de racketter des lvl au dessus du lvl 2, mais pas en lieux public, il faut avoir une raison (Recherche d'argent) et biensur faire une scène. Utilisez les /do /me très importants. Vous avez le droit de racketter jusqu’à 20 000$ maximum, il existe une banque il ne faudra pas se plaindre, si vous trouvez des substances drogues / matériaux, vous n'aviez pas le droit de prendre plus de la moitié.<br />
Il est interdit de kidnapper / rackétter en lieux public.<br />
Il est interdit de kidnapper un journaliste, pour éviter les abus, sauf si celui-ci traine dans le HQ / hood de votre faction.<br />
Attention, si vous prenez une victime dans votre véhicule, cela devient un kidnapping.
            </h5><br /><br />
            <h3><strong>Brawls :</strong></h3>
            <h5>
            	Les brawls ont déjà été évoqué dans le paragraphe des factions, je rapelle qu'il faut avoir une raison pour faire un brawl (initiation, provocation...) Si une faction commence un brawl, il ne doit y avoir QUE des armes blanches (couteau, batte, god, sabre (très rare)... ; pas d'armes à feu. Si la faction en face riposte avec des armes. Ils seront sanctionnés.
            </h5><br /><br />
            <h3><strong>Scène de sexe :</strong></h3>
            <h5>
            	Il est conseillé d’éviter les scènes de sexes pour la simple cause qu'il y'a des mineurs sur GTA San Andreas, vous pouvez en faire mais ceci est à éviter, si vous en abusez d'une manière, vous serez averti, puis sanctionné.
            </h5><br /><br />
            <h3><strong>Plusieurs sur la même IP :</strong></h3>
            <h5>
            	Si vous êtes plusieurs sur la même adresse Ip, il faut prévenir le staff, (frères, soeurs...) Si quelqu’un est bannis, il pourra pénalisé tout les autres. Le compte sera donc vérouillé, puis bannis s'il doit être.
            </h5><br /><br />
            <h3><strong>Changer de compte :</strong></h3>
            <h5>
            	Vous pouvez changer votre nom grace à des points de rename, ou bien en demandant à un admin de vous le changer.
            </h5><br /><br />
            <h3><strong>Tuez au poing :</strong></h3>
            <h5>
            	Lorsque vous tuez quelqu’un au poing, même avec une arme blanche, cela peut être du à une grosse bagarre, on ne meurt pas forcément lors d'une bagarre, donc penser à faire perdre la mémoire de votre victime en utilisant /me brise la nuke de "X" en mettant des coups de pied...
            </h5><br /><br />
            <h3><strong>AFK :</strong></h3>
            <h5>
            	Il est autorisé d'être AFK durant quelques minutes pour diverses raisons, un système d'anti AFK est mis en place pour éviter les abus, si vous abusez d'être AFK pour recevoir les Paydays par exemple, vous serez automatiquement kické.
            </h5><br /><br />
            <h3><strong>Vente de véhicule :</strong></h3>
            <h5>
            	Vous pouvez vendre vos véhicules achetés aux Concessionaires. La limite d'achat et de revente de véhicule entres joueurs est fixé à $500 000 maximum.<br />
Il y'a une exeption pour les véhicules coutent plus de $500 000, il est interdit de les vendre à un joueur au dessu des $500 000, la règle reste la même, on n'achète pas un hélicoptère pour en faire du buisness.
            </h5><br /><br />
            <h3><strong>Braquage :</strong></h3>
            <h5>
            Les braquages sont autorisé biensur avec des conditions.<br />
Pour braquer la banque il faut : 4 Policiers en service.<br />
Pour braquer la mairie il faut : 3 Policiers en service.<br />
Pour braquer un petit commerce (Ex: Pizzeria) : 3 Policiers en service.<br />
            </h5><br /><br />
            <h3><strong>Lieux Public :</strong></h3>
            <h5>
            	A Los Santos, comme partout il y a des lieux public, vous devrez bien garer vos véhicules sinon vous étonnez pas si vos véhicules se retrouvent à la fourrière. Les lieux publics d'abréviation "LP" sont les lieux ou tout le monde à accés. Exemple : 24/7; la Gare ; La Banque ; La mairie sont des LP. Les armes en LP sont : colt 45, uzi , tec 9 , silencieux et non LP : M4 , AK47 en gros les armes lourdes, elles sont strictement interdit.
            </h5><br /><br />
            <h3><strong>CarJack :</strong></h3>
            <h5>
            	Il est strictement interdit de volé le véhicule de quelqu'un sans l'utilisation de /me et /do, c'est à éviter en LP. Par exemple quelqu’un est à un stop, un homme toc à la vitre et lui parle, il utilisera comme /me : /me tente d’ouvrir d'un geste rapide et force la porte de la voiture.<br>
/do Reussi t-il ?<br>
Si le conducteur a réellement vérouillé le véhicule il fait : <br>
/do Non car la voiture est vérouiller. Si il vien de le vérouiller, cela est considéré comme mensonge OOC.
            </h5><br /><br />
            <h3><strong>Langues :</strong></h3>
            <h5>
            	Le serveur est francophone, la seule langue autorisée est le français, vous pouvez parler IG une autre langue si quelqu’un la connait, il existe une école de langues IG. Exemple si votre langue est l’allemand (vous jouer un roleplay allemand) (/all)emand On fait quoi ?
            </h5><br /><br />
            <h3><strong>Les publicités / Rendez vous :</strong></h3>
            <h5>
            	Les publicités et Rendez-vous se font totalement IG , il est interdit de parler / négocier de votre offre dans un chanel OOC. Pour les rendez-vous, apellé la personne au téléphone. C'est interdit de le faire via un chat OOC.
            </h5><br /><br />
            <h3><strong>Language SMS :</strong></h3>
            <h5>
            	Il est interdit d'utiliser le language SMS du style "stp", sauf en /t sms (pour envoyer un sms IG), N'oubliez pas qu'une phrase commence par une majuscule et se fini par un point.
            </h5><br /><br />
            <h3><strong>Publicité pour d'autre serveur :</strong></h3>
            <h5>
            	Il est strictement interdit de faire de la publicité pour d'autres serveurs sous peine de bannissement immédiat et déffinitif.
            </h5><br /><br />
            <h3><strong>Metagame par utilisation de logiciel :</strong></h3>
            <h5>
            	Le metagame par les logiciels de communication comme Skype ; TeamSpeak ; Mumble sont possible, il y'a des sanctions à ça, par exemple, vous vous faite raquetter, vous apellez des personnes pour venir vous défendre via Skype, et non IG ou sur teamspeak , il existe des "Radio : IC".
                </h5><br /><br />
            <h3><strong>Revenge Kill :</strong></h3>
            C'est quand vous mourrez par quelqu'un et que vous revenez sur les lieux pour tuer la personne . Attendez au moins quinze minutes pour revenir dans la scène .
            <h5><br /><br />
            <h3><strong>Chicken Run :</strong></h3>
            Le ChickenRun est de zigzagué ou de faire des ronds quand on vous tire dessus ou vous vous battez contre quelqu'un. En vrai vous n'allez pas Zigzagué mais tenté de fuir le plus rapidement possible sans chercher à comprendre si il faut rallonger son chemin ou autre. Donc réagissez de façon RolePlay.
            <h5><br /><br />
            <h3><strong>Le PowerGame :</strong></h3>
            C'est un acte irréaliste c'est-à-dire une action qui se réalise pas .
            <h5><br /><br />
            <h3><strong>Le Metagame :</strong></h3>
            C'est le fait d'utiliser des informations OOC en IC .        	
